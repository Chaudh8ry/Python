a = "1" 
b = "2"
print(a+b) # answer will be '12' bcz 'a' and 'b' are both strings

# Typecasting : converting one data type to another datatype
print(int(a) + int(b)) # this is Explicit Typecasting

# if a = "vishal" it cannot be typecasted into Integer bcz its not a valid integer

# Implicit Typecasting
c = 1.2
d = 3
print(c+d) # answer will be of FLOAT type