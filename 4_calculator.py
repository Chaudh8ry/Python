a = 5
b = 4

# Addition
print("The Value of",a, "+" ,b , "=",a+b) # 9
# Subtraction
print("The Value of",a, "-" ,b , "=",a-b) # 1
# Multiplication
print("The Value of",a, "*" ,b , "=",a*b) # 20
# Division
print("The Value of",a, "/" ,b , "=",a/b) # 1.25
# Exponentiation
print("The Value of",a, "**" ,b , "=",a**b) # 5^4
# Float Division
print("The Value of",a, "//" ,b , "=",a//b) # 1 (no decimal value)
# Modulo
print("The Value of",a, "%" ,b , "=",a%b) # 1